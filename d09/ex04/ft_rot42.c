char	*ft_rot42(char *str)
{
	char *tmp;

	tmp = str - 1;
	while (*++tmp)
		if (*tmp >= '!' && *tmp <= '~')
			*tmp = ((*tmp - '!') + 46) % ('~' - '!' + 1) + '!';
	return (str);
}
