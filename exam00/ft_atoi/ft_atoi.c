int	ft_atoi(const char *str)
{
	int mod;
	int nbr;

	nbr = 0;
	mod = 0;
	while (*str == ' ' || (*str >= '\a' && *str <= '\r'))
		str++;
	if (*str == '-' || *str == '+')
		mod = *str++ == '-';
	while (*str >= '0' && *str <= '9')
		nbr = nbr * 10 + (*str++ - '0');
	return (mod ? -nbr : nbr);
}
