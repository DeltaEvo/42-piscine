#include <unistd.h>

int	main(int argc, char *argv[])
{
	int i;

	if (argc == 2)
	{
		while (*argv[1] == ' ' || *argv[1] == '\t')
			argv[1]++;
		i = 0;
		while (argv[1][i] && argv[1][i] != ' ' && argv[1][i] != '\t')
			i++;
		write(1, argv[1], i);
	}
	write(1, "\n", 1);
}
