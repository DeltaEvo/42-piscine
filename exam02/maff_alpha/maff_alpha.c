#include <unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

int		main(void)
{
	int i;

	i = -1;
	while (++i < 26)
		ft_putchar(i % 2 ? 'A' + i : 'a' + i);
	ft_putchar('\n');
}
