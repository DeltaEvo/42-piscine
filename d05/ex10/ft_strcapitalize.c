char	*ft_strcapitalize(char *str)
{
	char *t;

	if (*str >= 'a' && *str <= 'z')
		*str ^= 0x20;
	t = str;
	while (*++t)
		if ((*t >= '0' && *t <= '9') || ((*t | 32) >= 'a' && (*t | 32) <= 'z') ?
*t >= 'A' && *t <= 'Z' : (*(t + 1) | 32) >= 'a' && (*(t + 1) | 32) <= 'z'
	&& t++ && *t >= 'a')
			*t ^= 0x20;
	return (str);
}
