#include <unistd.h>

int	main(int ac, char *av[])
{
	char	*w;
	int		i;

	if (ac == 2)
	{
		w = av[1];
		while (*w == ' ' || *w == '\t')
			w++;
		i = 0;
		while (w[i] && w[i] != ' ' && w[i] != '\t')
			i++;
		write(1, w, i);
	}
	write(1, "\n", 1);
}
